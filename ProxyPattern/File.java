public class File {
    private String file_name;
    // Constructor, Getters, and Setters
    public File(String file_name){
        this.file_name = file_name;
    }

    public void setFile(String file_name){
        this.file_name = file_name;
    }

    public String getFile(){
        return this.file_name;
    }
}
